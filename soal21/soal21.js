let nilai = Number(prompt('Masukan Nilai anda'))

if ( nilai >= 90){
    console.log('Grade anda adalah A')
} else if ( nilai >= 80){
    console.log('Grade anda adalah B')
} else if ( nilai >= 70){
    console.log('Grade anda adalah C')
} else if ( nilai >= 60){
    console.log('Grade anda adalah D')
} else {
    console.log('Grade anda adalah E')
}
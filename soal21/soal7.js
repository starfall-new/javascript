let a = 8.3
let c = parseFloat("3.2")

let tambah = a + c
let kurang = a - c
let kali = a * c
let bagi = a / c
let persen = a % c

console.log (`8.3 + 3.2 = ${tambah}`)
console.log (`8.3 - 3.2 = ${kurang}`)
console.log (`8.3 * 3.2 = ${kali}`)
console.log (`8.3 / 3.2 = ${bagi}`)
console.log (`8.3 % 3.2 = ${persen}`)
